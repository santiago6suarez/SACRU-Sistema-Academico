    public void limpiarTabla(JTable tabla, DefaultTableModel modelo) {
        try {
            int filas = tabla.getRowCount();
            for (int i = 0; i < filas; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

