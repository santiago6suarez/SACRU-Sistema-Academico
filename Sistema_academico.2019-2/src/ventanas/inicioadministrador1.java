    public Administrador recibir_usuario(String usuario) {
        Login objlogin = new Login();

        Administrador objadmin = objlogin.obtener_info_administrador(usuario, objconexion);
        nombreusuario.setText(objadmin.getNombre_1() + " " + objadmin.getNombre_2() + " " + objadmin.getApellido_1() + " " + objadmin.getApellido_2());
        camponombreadmin.setText(objadmin.getNombre_1() + " " + objadmin.getNombre_2() + " " + objadmin.getApellido_1() + " " + objadmin.getApellido_2());
        campotipodedocumento.setText(objadmin.getTipo_documento());
        camponumerodedocumento.setText(objadmin.getNumero_documento());
        campocorreopersonal.setText(objadmin.getCorreo_personal());
        campocorreoinstitucional.setText(objadmin.getCorreo_institucional());
        // String numCadena= Integer.toString(numEntero);
        String edad = Integer.toString(objadmin.getEdad());
        String numero_celular = Long.toString(objadmin.getNumero_celular());
        String codigo = Integer.toString(objadmin.getCodigo());
        campoedad.setText(edad);
        camposexo.setText(objadmin.getSexo());
        campocargo.setText(objadmin.getCargo());
        camponumerodecelular.setText(numero_celular);
        campocodigo.setText(codigo);

        return objadmin;
    }

