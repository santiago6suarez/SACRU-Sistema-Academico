    public void validacionCaracteres(java.awt.event.KeyEvent evento) {
        if (evento.getKeyChar() >= 32 && evento.getKeyChar() <= 64 || evento.getKeyChar() >= 91 && evento.getKeyChar() <= 96 || evento.getKeyChar() >= 123 && evento.getKeyChar() <= 208 || evento.getKeyChar() >= 210 && evento.getKeyChar() <= 240 || evento.getKeyChar() >= 242 && evento.getKeyChar() <= 255) {
            evento.consume();
            JOptionPane.showMessageDialog(null, "No se permiten caracteres especiales: '" + evento.getKeyChar() + "'");
        }

    }

    public boolean validarCamposVacios() {
        boolean vacio = false;
        //limpiar
        avisonombre.setText("");
        avisoprimerapellido.setText("");
        avisosegundoapellido.setText("");
        avisonumerodocumento.setText("");
        avisocorreopersonal1.setText("");
        avisocodigoestudiantil.setText("");
        avisoestado.setText("");
        if (camponombre2.getText().isEmpty()) {
            avisonombre.setText("**");
            vacio = true;
        }
        if (campoprimerapellido1.getText().isEmpty()) {
            avisoprimerapellido.setText("**");
            vacio = true;
        }
        if (camposegundoapellido1.getText().isEmpty()) {
            avisosegundoapellido.setText("**");
            vacio = true;
        }
        if (camponumerodedocumento2.getText().isEmpty()) {
            avisonumerodocumento.setText("**");
            vacio = true;
        }
        if (campocorreopersonal2.getText().isEmpty()) {
            avisocorreopersonal1.setText("**");
            vacio = true;
        } else if (!campocorreopersonal2.getText().contains("@") || !campocorreopersonal2.getText().contains(".")) {
            avisocorreopersonal1.setText("No válido.");
        }
        if (estado1.getText().isEmpty()) {
            avisoestado.setText("**");
            vacio = true;
        }
        if (codigo_estudiante1.getText().isEmpty()) {
            avisocodigoestudiantil.setText("**");
            vacio = true;
        }
        return vacio;
    }

