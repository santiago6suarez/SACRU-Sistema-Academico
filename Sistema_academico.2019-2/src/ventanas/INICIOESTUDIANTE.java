    public Estudiante recibir_usuario(String usuario) {
        Login objlogin = new Login();

        Estudiante objest = objlogin.obtener_info_estudiante(usuario, objconexion);

        nombreusuario.setText(objest.getNombre_1() + " " + objest.getNombre_2() + " " + objest.getApellido_1() + " " + objest.getApellido_2());
        nombrecompleto.setText(objest.getNombre_1() + " " + objest.getNombre_2() + "" + objest.getApellido_1() + " " + objest.getApellido_2());
        tipodocumento.setText(objest.getTipo_documento());
        numerodocumento.setText(objest.getNumero_documento());
        correopersonal.setText(objest.getCorreo_personal());
        correoinstitucional.setText(objest.getCorreo_institucional());
        estado.setText(objest.getEstado());
        programa.setText(objest.getObjprograma().getNombre());
        facultad.setText(objest.getObjprograma().getObjfacultad().getNombre());
        nombre_programa.setText(objest.getObjprograma().getNombre());

        return objest;
    }

