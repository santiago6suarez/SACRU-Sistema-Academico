    public String ruta_archivo() {
        JFileChooser dlg = new JFileChooser();
        int opcion = dlg.showSaveDialog(this);
        if (opcion == JFileChooser.APPROVE_OPTION) {
            File f = dlg.getSelectedFile();
            String ruta = f.toString();
            return ruta;

        }
        return null;
    }
    private void botonayuda1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonayuda1ActionPerformed
        
        generarpdf g = new generarpdf();
        Liquidacionderecibo liq = new Liquidacionderecibo();
        Estudiante est = recibir_usuario(usuario_online);
        boolean descarga = liq.validar_recibo(est.getNumero_documento(), objconexion);
        if (descarga) {
            String ruta = ruta_archivo();
            String titulo = "UNIVERSIDAD DE LOS LLANOS \n "
                    + "TALENTO Y CONOCIMIENTO PARA EL DESARROLLO REGIONAL ";

            String cuerpo = liq.informacion_recibo_pdf(est.getNumero_documento(), objconexion);

            g.generarPDF(titulo, cuerpo, "Universidad de los Llanos © Todos los derechos reservados ", "C:\\Users\\ADMIN\\Pictures\\unillanos.png", ruta);
            JOptionPane.showMessageDialog(null, "recibo de liquidacion descargado");

        } else {
            JOptionPane.showMessageDialog(null, "no puedes descargar tu matricula");

        }


    }//GEN-LAST:event_botonayuda1ActionPerformed

