    private void botonguardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonguardar1ActionPerformed
        boolean bandera = validarCamposVacios();
        if (!bandera) {
            Estudiante est = new Estudiante();
            //obtenemos la informacion

            Date fecha = fecha1.getDate();

            DateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            String fecha_ingreso = formato.format(fecha);

            int año = fecha1.getCalendar().get(Calendar.YEAR);

            String nombre_1 = camponombre2.getText();
            String nombre_2 = camposegundonombre1.getText();
            String apellido_1 = campoprimerapellido1.getText();
            String apellido_2 = camposegundoapellido1.getText();
            String num_documento = camponumerodedocumento2.getText();
            String tipo_documento = campotipodedocumento2.getSelectedItem().toString();
            int edad_est = Integer.parseInt(edad_estudiante1.getValue().toString());
            String Sexo = camposexo2.getSelectedItem().toString();
            String Programa = campoprograma1.getSelectedItem().toString();

            String correo_personal = campocorreopersonal2.getText();

            int codigo_est = Integer.parseInt(codigo_estudiante1.getText());
            //System.out.println(codigo_est);
            String Estado = estado1.getText();
            int periodo = Integer.parseInt(periodo_ingreso1.getValue().toString());

            //hacemos las validacones
            String correo_institucional = est.validar_correo_institucional(nombre_1, apellido_1, apellido_2, objconexion);
            // boolean validacion_correo= est.validar_correo_institucional(correo_institucional);

            est.cargar_estudiantes(nombre_1, nombre_2, apellido_1, apellido_2, tipo_documento, num_documento, edad_est, Sexo, correo_personal, correo_institucional, codigo_est, Estado, Programa, año, periodo, fecha_ingreso, objconexion);

            //limpiamos los componentes despues de haber cargado el estudiante
            camponombre2.setText("");
            camposegundonombre1.setText("");
            campoprimerapellido1.setText("");
            camposegundoapellido1.setText("");
            camponumerodedocumento.setText("");
            campocorreopersonal.setText("");
            codigo_estudiante1.setText("");
            estado1.setText("");
            campocorreopersonal2.setText("");
        } else {

            JOptionPane.showMessageDialog(null, "Digite los campos obligatorios (**).");
        }
    }//GEN-LAST:event_botonguardar1ActionPerformed

